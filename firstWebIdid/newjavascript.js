function botonesAmpliarReducir() {
    console.log('conectar onclick');
    document.getElementById('hipervAmpliarN1').onclick = function (e) {
        e.preventDefault();
        var hipervinculo = document.getElementById('ampliacionN1');
        if (hipervinculo.style.display === 'none' || hipervinculo.style.display === "") {
            console.log('ampliando...');
            hipervinculo.style.display = 'block';
            this.innerHTML = "Reducir ...";
        } else {
            hipervinculo.style.display = 'none';
            this.innerHTML = "Ampliar ...";
            console.log('reduciendo...');
        }
    };
    document.getElementById('hipervAmpliarN2').onclick = function () {
        var hipervinculo = document.getElementById('ampliacionN2');
        if (hipervinculo.style.display === 'none' || hipervinculo.style.display === "") {
            hipervinculo.style.display = 'block';
            this.innerHTML = "Reducir ...";
        } else {
            hipervinculo.style.display = 'none';
            this.innerHTML = "Ampliar ...";
        }
    };
    document.getElementById('hipervAmpliarN3').onclick = function () {
        var hipervinculo = document.getElementById('ampliacionN3');
        if (hipervinculo.style.display == 'none' || hipervinculo.style.display == "") {
            hipervinculo.style.display = 'block';
            this.innerHTML = "Reducir ...";
        } else {
            hipervinculo.style.display = 'none';
            this.innerHTML = "Ampliar ...";
        }
    };
    document.getElementById('hipervAmpliarN4').onclick = function () {
        var hipervinculo = document.getElementById('ampliacionN4');
        if (hipervinculo.style.display == 'none' || hipervinculo.style.display == "") {
            hipervinculo.style.display = 'block';
            this.innerHTML = "Reducir ...";
        } else {
            hipervinculo.style.display = 'none';
            this.innerHTML = "Ampliar ...";
        }
    };
}
